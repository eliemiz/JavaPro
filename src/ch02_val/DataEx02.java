package ch02_val;

public class DataEx02 {

	public static void main(String[] args) {
		
		byte var1 = (byte)128;			// 범위 밖의 수이므로
		short var2 = 128;
		int var3 = 128;					// 128l은 long 타입이므로
		long var4 = 128l;
		float var5 = 123456.789123f;	// 실수의 기본 리터럴은 double형이므로
		double var6 = 123456.789123;
		
		
	}
}
